# 7. Price Snapshots
# ---------------------------------------------------------------------------


class PriceSnapshot(Base):
    """Spot price of a tracked crypto asset at 1-minute granularity.

    Sourced from CoinGecko or Binance and used by strategy evaluation and
    the risk manager.  Data is also cached in Redis for fast reads.
    """

    __tablename__ = "price_snapshots"
    __table_args__ = (
        Index("ix_ps_crypto_ts", "crypto", "timestamp"),
    )

    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    crypto: Mapped[str] = mapped_column(
        String(10), nullable=False, index=True,
        comment="BTC, ETH, or SOL.",
    )
    price_usd: Mapped[float] = mapped_column(Float, nullable=False)
    source: Mapped[str] = mapped_column(
        String(20), nullable=False,
        comment="coingecko | binance.",
    )
    timestamp: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, index=True,
    )

    def __repr__(self) -> str:
        return (
            f"<PriceSnapshot id={self.id} crypto={self.crypto} "
            f"price={self.price_usd} source={self.source!r}>"
        )


# ---------------------------------------------------------------------------
# 8. System State
# ---------------------------------------------------------------------------


class SystemState(Base):
    """Key/value store for persisting runtime engine state across restarts.

    Examples: last scan timestamp, daily P&L accumulator, kill-switch state.
    Values are stored as JSONB to accommodate arbitrary data shapes.
    """

    __tablename__ = "system_state"

    key: Mapped[str] = mapped_column(
        String(100), primary_key=True,
        comment="Unique state key, e.g. 'last_scan_at', 'daily_pnl'.",
    )
    value: Mapped[dict[str, Any]] = mapped_column(
        JSONB, nullable=False, default=dict,
        comment="Arbitrary JSON payload.",
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, server_default=_NOW, onupdate=_NOW,
    )

    def __repr__(self) -> str:
        return f"<SystemState key={self.key!r}>"


# ---------------------------------------------------------------------------
# 9. Strategy Configs
# ---------------------------------------------------------------------------


class StrategyConfig(Base):
    """Persisted per-strategy, per-market parameter overrides.

    Hierarchy:
    * ``market_id=None AND crypto=None`` — global default for the strategy
    * ``market_id=None AND crypto='BTC'`` — default for all BTC markets
    * ``market_id=<id>`` — override for a specific market

    The engine resolves the most specific config when evaluating a market.
    """

    __tablename__ = "strategy_configs"
    __table_args__ = (
        Index("ix_sc_strategy_market", "strategy", "market_id"),
    )

    id: Mapped[int] = mapped_column(Integer, primary_key=True, autoincrement=True)
    strategy: Mapped[str] = mapped_column(
        String(50), nullable=False, index=True,
        comment="Strategy name: volatility_spread | stink_bid | liquidity_sniper.",
    )
    market_id: Mapped[int | None] = mapped_column(
        Integer, ForeignKey("markets.id", ondelete="CASCADE"), nullable=True, index=True,
        comment="NULL = applies to all markets (default).",
    )
    crypto: Mapped[str | None] = mapped_column(
        String(10), nullable=True,
        comment="NULL = applies to all cryptos.",
    )
    enabled: Mapped[bool] = mapped_column(
        Boolean, nullable=False, default=True,
        comment="Whether this strategy is active for the given scope.",
    )
    params: Mapped[dict[str, Any]] = mapped_column(
        JSONB, nullable=False, default=dict,
        comment="Strategy-specific parameter overrides.",
    )
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, server_default=_NOW,
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), nullable=False, server_default=_NOW, onupdate=_NOW,
    )

    # Relationships
    market: Mapped[Market | None] = relationship(
        "Market", back_populates="strategy_configs"
    )

    def __repr__(self) -> str:
        return (
            f"<StrategyConfig id={self.id} strategy={self.strategy!r} "
            f"market_id={self.market_id} enabled={self.enabled}>"
        )
